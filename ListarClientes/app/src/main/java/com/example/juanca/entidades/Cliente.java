package com.example.juanca.entidades;

import java.io.Serializable;


/**
 *
 * @author Juanca
 */

public class Cliente implements Serializable {
    private static final long serialVersionUID = 1L;

    private String cedula;

    private String nombre;

    private String direccion;

    private String telefono;

    private String celular;


    private EstadoServicio numeroOrden;


    public Cliente() {
    }

    public Cliente(String cedula) {
        this.cedula = cedula;
    }

    public Cliente(String cedula, String nombre, String direccion, String telefono) {
        this.cedula = cedula;
        this.nombre = nombre;
        this.direccion = direccion;
        this.telefono = telefono;
    }

    public Cliente(String cedula, String nombre) {

    }

    public String getCedula() {
        return cedula;
    }

    public void setCedula(String cedula) {
        this.cedula = cedula;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getDireccion() {
        return direccion;
    }

    public void setDireccion(String direccion) {
        this.direccion = direccion;
    }

    public String getTelefono() {
        return telefono;
    }

    public void setTelefono(String telefono) {
        this.telefono = telefono;
    }

    public String getCelular() {
        return celular;
    }

    public void setCelular(String celular) {
        this.celular = celular;
    }

    public EstadoServicio getNumeroOrden() {
        return numeroOrden;
    }

    public void setNumeroOrden(EstadoServicio numeroOrden) {
        this.numeroOrden = numeroOrden;
    }

    @Override
    public int hashCode() {
        int hash = 0;
        hash += (cedula != null ? cedula.hashCode() : 0);
        return hash;
    }

    @Override
    public boolean equals(Object object) {
        // TODO: Warning - this method won't work in the case the id fields are not set
        if (!(object instanceof Cliente)) {
            return false;
        }
        Cliente other = (Cliente) object;
        if ((this.cedula == null && other.cedula != null) || (this.cedula != null && !this.cedula.equals(other.cedula))) {
            return false;
        }
        return true;
    }

    @Override
    public String toString() {
        return "lavacor.entidades.Cliente[ cedula=" + cedula + " ]";
    }
    
}
